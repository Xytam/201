# Inserts a given number into a list so it is still ordered

#init variables
notInserted='true'
i=0
myList=[1,9,16,24]
#Read in input
num=int(input("Enter a number: "))
#Loops until the input is inserted
while(notInserted=='true'):
    #If the input is greater, keep trying next index
    #If not,insert the number at that index
    if(num>myList[i]):
        i+=1
        pass
    else:
        myList.insert(i,num)
        notInserted='false'
print(myList)
